# Creating a new list
list = ["Keyboard", "CPU", "Mouse"]
# Adding "Printer at the last using append function"
list.append("Printer")
# Inserting Monitor at index 2 using insert function
list.insert(2, "Monitor")
print(list)
# Printing length of the list
print(len(list))
# Removing "Mouse" using remove function
list.remove("Mouse")
print(list)
# Printing length of the list
print(len(list))
